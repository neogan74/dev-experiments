package main

import (
	"fmt"
)

func removeDuplicates(nums []int) int {
	// Если массив пустой, возвращаем 0
	if len(nums) == 0 {
		return 0
	}
	// Первый элемент всегда уникален
	i := 0
	// Проходим по массиву начиная со второго элемента
	for j := 1; j < len(nums); j++ {
		// Если текущий элемент отличается от последнего уникального
		if nums[j] != nums[i] {
			i++               // Перемещаем указатель уникального элемента
			nums[i] = nums[j] // Записываем новый уникальный элемент в массив
		}
	}
	// Возвращаем количество уникальных элементов
	return i + 1
}

func main() {
	nums := []int{0, 0, 1, 1, 1, 2, 2, 3, 3, 4}
	k := removeDuplicates(nums)
	fmt.Println("Количество уникальных элементов:", k)
	fmt.Println("Массив после удаления дубликатов:", nums[:k])
}
